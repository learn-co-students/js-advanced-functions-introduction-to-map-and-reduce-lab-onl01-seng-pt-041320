//Map-Like Functions
function mapToNegativize(arr){
    return arr.map(x => x * (-1));
}

function mapToNoChange(arr){
    return arr.map(x => x);
}

function mapToDouble(arr){
    return arr.map(x => x * 2);
}

function mapToSquare(arr){
    return arr.map(x => x**2);
}

